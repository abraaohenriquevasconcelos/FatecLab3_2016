package hello;

public class Chamado {

	private String numeroChamado;
	private String nomeUsuarioChamado;
	private String descricaoChamado;
	private String dataChamado;
	private String situacaoChamado;
	public String getNumeroChamado() {
		return numeroChamado;
	}
	public void setNumeroChamado(String numeroChamado) {
		this.numeroChamado = numeroChamado;
	}
	public String getNomeUsuarioChamado() {
		return nomeUsuarioChamado;
	}
	public void setNomeUsuarioChamado(String nomeUsuarioChamado) {
		this.nomeUsuarioChamado = nomeUsuarioChamado;
	}
	public String getDescricaoChamado() {
		return descricaoChamado;
	}
	public void setDescricaoChamado(String descricaoChamado) {
		this.descricaoChamado = descricaoChamado;
	}
	public String getDataChamado() {
		return dataChamado;
	}
	public void setDataChamado(String dataChamado) {
		this.dataChamado = dataChamado;
	}
	
	public String getSituacaoChamado(){
		return situacaoChamado;
	}
	public void setSituacaoChamado(String situacaoChamado){
		this.situacaoChamado = situacaoChamado;
	}
	
	
	
}